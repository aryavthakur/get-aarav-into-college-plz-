"""
Report Generator — Institutional Biotech Diligence Memo.

Converts an AuditResponse into a structured Markdown document suitable for
investment committee review, internal diligence memos, and fund analysis.

Tone: institutional, analytical, risk-aware, scientifically cautious.
This is NOT investment advice. All outputs are model estimates.
"""

from __future__ import annotations

from typing import List

from app.models.schemas import AuditRequest, AuditResponse


def _fmt_m(value: float) -> str:
    """Format USD value in millions with 1 decimal."""
    return f"${value / 1_000_000:.1f}M"


def _fmt_pct(value: float) -> str:
    return f"{value:.1%}"


def _fmt_months(value: float) -> str:
    return f"{value:.1f} months"


def _risk_color_label(risk: str) -> str:
    mapping = {
        "Low Risk": "**Low Risk**",
        "Moderate Risk": "**Moderate Risk**",
        "High Risk": "**High Risk**",
        "Critical Risk": "**Critical Risk**",
    }
    return mapping.get(risk, risk)


