def _capital_runway_section(r: AuditResponse, req: AuditRequest) -> str:
    sol = r.solvency
    fin = req.financial

    covariate_rows = "\n".join(
        f"| {k.replace('_', ' ').title()} | {v:+.4f} |"
        for k, v in sol.covariate_contributions.items()
    )

    # Build survival table (selected months)
    survival_months = [3, 6, 9, 12, 18, 24, 36, 48]
    curve_dict = {sp.month: sp for sp in sol.survival_curve}
    survival_rows = ""
    for m in survival_months:
        if m in curve_dict:
            sp = curve_dict[m]
            survival_rows += (
                f"| {m} | {_fmt_pct(sp.survival_probability)} | "
                f"{_fmt_pct(sp.implied_cashout_risk)} |\n"
            )

    total_liq = sol.total_liquidity
    market_cap = fin.market_cap

    return f"""## C. Capital Structure and Runway Analysis

### Liquidity and Burn Summary

| Metric | Value | Notes |
|---|---|---|
| Cash and Equivalents | {_fmt_m(fin.cash_on_hand)} | Reported cash position |
| Marketable Securities | {_fmt_m(fin.marketable_securities)} | Available secondary liquidity |
| **Total Modelled Liquidity** | **{_fmt_m(total_liq)}** | Primary analysis base |
| Quarterly Operating Burn | {_fmt_m(fin.quarterly_operating_cash_burn)} | Most recent quarter |
| Monthly Burn Estimate | {_fmt_m(sol.monthly_burn)} | Quarterly ÷ 3 |
| Debt | {_fmt_m(fin.debt)} | Reported obligations |
| Market Capitalisation | {_fmt_m(market_cap)} | Reference for refinancing capacity |
| Going-Concern Language | {"Yes — elevated risk flag" if fin.going_concern_flag else "Not detected"} | SEC filing indicator |
| Recent Financing (≤6mo) | {"Yes" if fin.recent_financing_flag else "No"} | Capital raise recency |
| Months Since Last Raise | {fin.months_since_last_raise:.0f} months | Financing runway context |

### Simple vs Modelled Runway

**Simple runway** (cash / burn) = **{sol.simple_runway_months:.1f} months**

Simple runway provides a static estimate of liquidity duration assuming
constant burn and no external financing. The Cox-style survival model adjusts
this estimate by incorporating financing risk covariates — burn acceleration,
market conditions, debt burden, market capitalisation relative to cash, and
going-concern status — that may accelerate or reduce the probability of capital
exhaustion.

**Modelled median financial failure time** = **{sol.median_failure_time:.1f} months**
**Cox risk multiplier** = **{sol.risk_multiplier:.3f}x** baseline hazard

> A risk multiplier above 1.0 indicates this company faces higher-than-baseline
> financing failure risk. A multiplier below 1.0 indicates lower-than-baseline risk.

### Cox Covariate Contributions to Risk

| Covariate | Log-Hazard Contribution |
|---|---:|
{covariate_rows}
| **Total Linear Predictor** | **{sol.linear_predictor:+.4f}** |

### Modelled Survival Curve

| Month | Survival Probability | Implied Cashout Risk |
|---:|---:|---:|
{survival_rows}

> **Model note:** These survival probabilities represent the modelled probability
> that the company retains access to capital at each time horizon. They do NOT
> incorporate the probability of technical success or pipeline value. They reflect
> financing survival only."""


def _burn_regime_section(r: AuditResponse, req: AuditRequest) -> str:
    br = r.burn_regime
    fin = req.financial

    if not br.burn_series:
        return "## D. Burn Rate and Regime Analysis\n\n_Insufficient burn history data._"

    rows = ""
    for i, (q, burn) in enumerate(zip(br.quarters, br.burn_series)):
        qoq = br.quarterly_pct_changes[i]
        qoq_str = f"{qoq:+.1%}" if qoq is not None else "—"
        rows += f"| {q} | {_fmt_m(burn)} | {qoq_str} |\n"

    cp_note = (
        f"Change point(s) detected at quarter index(es): {br.changepoint_indices}"
        if br.changepoint_indices
        else "No structural break detected."
    )

    return f"""## D. Burn Rate Quality and Regime Shift Analysis

### Quarterly Burn History

| Quarter | Operating Cash Burn | QoQ Change |
|---|---:|---:|
{rows}

**Detected Change Points:** {cp_note}
**Mean QoQ Burn Acceleration:** {br.burn_acceleration:+.1%}
**Current Burn Regime:** {br.regime.upper()}

### Regime Interpretation

{br.regime_interpretation}

> **Analytical note:** Burn acceleration is particularly relevant in
> clinical-stage biotechnology because transition from dose escalation to
> expansion cohorts, Phase 1 to Phase 2 conversion, or single-site to
> multi-site activation can materially increase operating cash consumption
> without being captured in trailing burn averages. Stated runway guidance
> based on prior-period burn may therefore substantially overstate remaining
> time under an accelerating regime."""


