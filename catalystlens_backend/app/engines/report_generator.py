def _disclosure_section(r: AuditResponse, req: AuditRequest) -> str:
    disc = r.disclosure_consistency

    gap_rows = ""
    all_keys = sorted(set(disc.narrative_normalized.keys()) | set(disc.audit_normalized.keys()))
    for key in all_keys:
        narr = disc.narrative_normalized.get(key, 0.0)
        aud = disc.audit_normalized.get(key, 0.0)
        gap = disc.category_gaps.get(key, 0.0)
        gap_str = f"{gap:+.3f}"
        gap_rows += f"| {key.replace('_', ' ').title()} | {narr:.3f} | {aud:.3f} | {gap_str} |\n"

    return f"""## I. Disclosure Consistency / Narrative Gap Analysis

The disclosure consistency module compares the company's narrative framing
(as represented by probability-like scores over risk categories) with the
structured quantitative model output using Jensen-Shannon Divergence (JSD).

JSD is symmetric and bounded [0, 1]: 0 = perfect alignment, 1 = complete divergence.

### Category-Level Gap Table

| Category | Narrative Score | Structured Audit | Gap (N−A) |
|---|---:|---:|---:|
{gap_rows}

### Divergence Metrics

| Metric | Value |
|---|---:|
| Jensen-Shannon Divergence | {disc.jsd_score:.4f} |
| Mean Absolute Raw Gap | {disc.mean_absolute_gap:.4f} |
| Optimism Bias | {disc.optimism_bias:+.4f} |
| Maximum Category Gap | {disc.max_category_gap:.4f} |
| Combined Gap Score | {disc.combined_gap_score:.4f} |
| KL(Narrative ∥ Audit) | {disc.kl_narrative_vs_audit:.4f} |
| KL(Audit ∥ Narrative) | {disc.kl_audit_vs_narrative:.4f} |
| **Disclosure Gap Classification** | **{disc.gap_classification.upper()}** |

### Interpretation

{disc.interpretation}

> **Important:** A material disclosure gap does not imply misconduct, selective
> disclosure, or misleading communications. It indicates that management's
> narrative framing of the company's risk profile is materially more favourable
> than the structured quantitative model estimates. Investors should weigh this
> divergence when evaluating forward guidance and stated milestones."""


