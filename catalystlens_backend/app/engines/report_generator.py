def _executive_summary(r: AuditResponse, req: AuditRequest) -> str:
    s = r.final_summary
    ctc = r.capital_to_catalyst
    sol = r.solvency
    pos = r.success_probability
    val = r.valuation
    clin = req.clinical

    # Investment-style interpretation
    cashout_pct = _fmt_pct(ctc.probability_cashout_before_catalyst)
    pos_pct = _fmt_pct(pos.posterior_mean)

    if ctc.probability_cashout_before_catalyst >= 0.60:
        fin_interp = (
            f"The company's lead program presents a materially constrained financing profile. "
            f"Under the current burn trajectory, the modelled probability of capital exhaustion "
            f"before the stated {clin.catalyst_type.replace('_', ' ')} is {cashout_pct}. "
            f"The investment case therefore appears less dependent on technical failure alone "
            f"and more dependent on the company's ability to finance through clinical data "
            f"without excessive dilution."
        )
    elif ctc.probability_cashout_before_catalyst >= 0.35:
        fin_interp = (
            f"The company presents moderate capital-to-catalyst risk. While the modelled "
            f"probability of reaching the {clin.catalyst_type.replace('_', ' ')} before "
            f"cash exhaustion is {_fmt_pct(ctc.probability_reaches_catalyst)}, there remains "
            f"a {cashout_pct} probability that refinancing will be required before the data "
            f"event. Dilution risk is a non-trivial component of the risk profile."
        )
    else:
        fin_interp = (
            f"The company appears sufficiently capitalised to reach the modelled catalyst "
            f"window under base-case assumptions. The primary risk appears to be technical "
            f"rather than financial, with a {cashout_pct} modelled probability of capital "
            f"exhaustion before the {clin.catalyst_type.replace('_', ' ')}."
        )

    return f"""## A. Executive Investment Summary

{fin_interp}

### Summary of Key Questions

| Question | Finding |
|---|---|
| What is the company claiming? | {sol.simple_runway_months:.0f}-month runway; {clin.stated_months_to_catalyst:.0f}-month stated catalyst timeline |
| What does the model estimate? | {sol.median_failure_time:.0f}-month modelled financial clock; {r.milestone_timing.p50_months:.0f}-month modelled catalyst |
| Where is the main risk? | {s.primary_risk_factor} |
| Is the limit science, timing, or capital? | {s.primary_risk_factor} (primary), {s.secondary_risk_factor} (secondary) |
| Overall risk classification | {_risk_color_label(s.risk_classification)} |

### Key Finding

{s.key_finding}

### Critical Metric

> **Modelled probability of cash exhaustion before stated catalyst: {cashout_pct}**
> **Modelled Bayesian posterior PoS: {pos_pct}**
> **Financing-adjusted rNPV: {_fmt_m(val.financing_adjusted_rnpv)}**"""


